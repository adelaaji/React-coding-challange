import Head from "next/head";

import { useEffect, useState } from "react";
import { getPhotos } from "../api/unsplash";
import useInfiniteScroll from "../hooks/useInfinite";
import styles from "../styles/Index.module.scss";
import Photo from "../components/Photo";
import { ImSpinner9 } from "react-icons/im";

//
export default function Home() {
  const [data, setData] = useState([]);
  const [page, setPage] = useState(1);
  const [isFetching, setIsFetching] = useInfiniteScroll(moreData);

  useEffect(() => {
    loadData();
  }, []);

  const loadData = () => {
    getPhotos().then((data) => {
      console.log("results", data.response.results);
      setData(data.response.results);
    });
  };

  function moreData() {
    getPhotos(page).then((res) => {
      setData([...data, ...res.response.results]);
      setPage(page + 1);
      setIsFetching(false);
    });
    // let url = `https://medrum.herokuapp.com/feeds/?source=5718e53e7a84fb1901e05971&page=${page}&sort=latest`;
    // axios.get(url).then((res) => {
    //   setData([...data, ...res.data]);
    //   setPage(page + 1);
    //   setTimeout(() => {
    //     setIsFetching(false);
    //   }, 4000);
    // });
  }
  return (
    <div>
      <Head>
        <title>Photo Gallery</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/fav.ico" />
      </Head>

      {isFetching && (
        <div className={styles.loadingSpinner}>
          <ImSpinner9 size={40} />
        </div>
      )}
      <div className={styles.mainContainer}>
        <div className={styles.photosGrid}>
          {data.map((photo, index) => (
            <Photo photo={photo} key={index}></Photo>
          ))}
        </div>
      </div>
    </div>
  );
}
